// HEADER SPAN
let headerSpan = document.querySelectorAll('.header-span');

// Wallet onclick
headerSpan[0].onclick = function() {
    for(let i=0; i<headerSpan.length; i++) {
        headerSpan[i].style.borderColor = "transparent";
        headerSpan[i].style.color = "#707070";
    }
    headerSpan[0].style.borderColor = "#991D2B";
    headerSpan[0].style.color = "#991D2B";
}

